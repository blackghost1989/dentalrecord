export interface ToothDef {
    id: string; // Triadan number, e.g., "104"
    label: string;
    x: number; // Percentage X relative to chart width
    y: number; // Percentage Y relative to chart height
    isMaxillary: boolean;
    quadrant: 1 | 2 | 3 | 4;
}

